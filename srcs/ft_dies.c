#include "ft_printf.h"

void	ft_dies(t_id *e)
{
	if (id[0] == 'o')
		ft_strjoin("0", e->s);
	if (id[0] == 'x')
		ft_strjoin("0x", e->s);
	if (id[0] == 'X')
		ft_strjoin("0X", e->s);
	if ((id[0] == 'e' || id[0] == 'f' || id[0] == 'g') && (e->p[0] == 0))
	{
		ft_strjoin(e->s, '.');
	}
}
