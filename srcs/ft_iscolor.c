void	ft_iscolor(unsigned long *i, char *s, char *id)
{
	if (!ft_strchr(s + i[0], (int)'}'))
		return ;
	i[0] += (s[i[0]] == '}') ? 1 : 0;
	i[1] = i[0];
}
