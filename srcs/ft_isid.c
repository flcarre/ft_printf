int	ft_isid(char c)
{
	if (c == 'c' || c == 's' || c == 'p')
		return (1);
	if (c == 'd' || c == 'i' || c == 'o' || c == 'u' || c == 'x' || c == 'X')
		return (2);
	if (c == 'e' || c == 'f')
		return (3);
	if (c == '%')
		return (4);
	if (c == '{')
		return (5);
	return (0);
}
